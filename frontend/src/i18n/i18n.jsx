const LanguageContext = createContext(null);

const interpolate = (template, params = {}) =>
  String(template).replace(/\{(\w+)\}/g, (_, key) => (params[key] ?? ''));

export const LanguageProvider = ({ children }) => {
  const [lang, setLang] = useState(() => localStorage.getItem('doc:lang') || 'en');

  useEffect(() => {
    localStorage.setItem('doc:lang', lang);
    if (typeof document !== 'undefined') {
      document.documentElement.lang = lang;
    }
  }, [lang]);

  const t = useMemo(
    () => (key, params, fallback) => {
      const entry = TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.en?.[key] ?? fallback ?? key;
      return interpolate(entry, params);
    },
    [lang]
  );

  const value = useMemo(() => ({ lang, setLang, t }), [lang, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) {
    throw new Error('useI18n must be used within LanguageProvider');
  }
  return ctx;
};
